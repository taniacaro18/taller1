hombres = int(input("ingrese el numero de hombres"))
mujeres = int(input("ingrese el numero de mujeres"))

total = hombres + mujeres

porsentajehombres = hombres / total * 100
porsentajemujeres = mujeres / total * 100

print ("el porcentaje de hombres es de", porsentajehombres)
print ("el porcentaje de mujeres es de", porsentajemujeres)